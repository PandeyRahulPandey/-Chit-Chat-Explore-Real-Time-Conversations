import useGetConversations from "../../hooks/useGetConversations";
import { getRandomEmoji } from "../../utils/emojis";
import Conversation from "./Conversation";

const Conversations = () => {
	const { loading, conversations } = useGetConversations();
	return (
		<div className='py-2 flex flex-col overflow-auto'>
			{conversations.map((conversation, idx) => (
				<Conversation
					key={conversation._id}
					conversation={conversation}
					emoji={getRandomEmoji()} // function to get random emojis--> present in emojis.js
					lastIdx={idx === conversations.length - 1}  // returning last index, if index is last index then no divider is shown for it else for other entries , divider is shown after each entry.
				/>
			))}
        {/* LOADING SPINNER--> if it is loading then show the loading spinner for a second otherwise return NULL */}
			{loading ? <span className='loading loading-spinner mx-auto'></span> : null}
		</div>
	);
};
export default Conversations;

